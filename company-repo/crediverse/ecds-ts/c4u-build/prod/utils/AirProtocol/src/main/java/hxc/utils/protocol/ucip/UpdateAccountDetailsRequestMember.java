	{
		this.subscriberNumber = subscriberNumber;
	}

	@Override
	public String getOriginOperatorID()
	{
		return originOperatorID;
	}

	@Override
	public void setOriginOperatorID(String originOperatorID)
	{
		this.originOperatorID = originOperatorID;
	}

	public String getAccountPrepaidEmptyLimitAction()
	{
		return accountPrepaidEmptyLimitAction;
	}

	public void setAccountPrepaidEmptyLimitAction(String accountPrepaidEmptyLimitAction)
	{
		this.accountPrepaidEmptyLimitAction = accountPrepaidEmptyLimitAction;
	}

