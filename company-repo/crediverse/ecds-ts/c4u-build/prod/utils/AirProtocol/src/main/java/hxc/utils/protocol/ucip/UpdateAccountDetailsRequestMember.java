	{
		return firstIVRCallDoneFlag;
	}

	public void setFirstIVRCallDoneFlag(Boolean firstIVRCallDoneFlag)
	{
		this.firstIVRCallDoneFlag = firstIVRCallDoneFlag;
	}

	public String getExternalData1()
	{
		return externalData1;
	}

	public void setExternalData1(String externalData1)
	{
		this.externalData1 = externalData1;
	}

	public String getExternalData2()
	{
		return externalData2;
	}

	public void setExternalData2(String externalData2)
	{
		this.externalData2 = externalData2;
	}

	public Integer getAccountHomeRegion()
	{
		return accountHomeRegion;
	}

	public void setAccountHomeRegion(Integer accountHomeRegion)
	{
		this.accountHomeRegion = accountHomeRegion;
	}

	public String getPinCodeOriginal()
	{
		return pinCodeOriginal;
	}

	public void setPinCodeOriginal(String pinCodeOriginal)
	{
		this.pinCodeOriginal = pinCodeOriginal;
	}

	public Boolean getPinCodeValidationFlag()
	{
		return pinCodeValidationFlag;
	}

	public void setPinCodeValidationFlag(Boolean pinCodeValidationFlag)
	{
		this.pinCodeValidationFlag = pinCodeValidationFlag;
	}

	public String getPinCode()
	{
		return pinCode;
	}

	public void setPinCode(String pinCode)
	{
		this.pinCode = pinCode;
	}

	public Integer getUssdEndOfCallNotificationID()
	{
		return ussdEndOfCallNotificationID;
	}

	public void setUssdEndOfCallNotificationID(Integer ussdEndOfCallNotificationID)
	{
		this.ussdEndOfCallNotificationID = ussdEndOfCallNotificationID;
	}

	public Integer getLanguageIDCurrent()
	{
		return languageIDCurrent;
	}

	public void setLanguageIDCurrent(Integer languageIDCurrent)
	{
		this.languageIDCurrent = languageIDCurrent;
	}

	public String getAccountTimeZone()
	{
		return accountTimeZone;
	}

	public void setAccountTimeZone(String accountTimeZone)
	{
		this.accountTimeZone = accountTimeZone;
	}

