package hxc.utils.processmodel;

public abstract class MethodCall extends Action
{

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Fields
	//
	// /////////////////////////////////
	protected Action errorAction;

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Properties
	//
	// /////////////////////////////////
	public Action getErrorAction()
	{
		return errorAction;
	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Construction
	//
	// /////////////////////////////////
	public MethodCall(Action afterAction, Action errorAction)
	{
		super(afterAction);
		this.errorAction = errorAction;
	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Methods
	//
	// /////////////////////////////////

}
