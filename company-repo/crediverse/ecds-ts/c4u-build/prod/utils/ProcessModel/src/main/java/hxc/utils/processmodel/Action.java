package hxc.utils.processmodel;

import hxc.utils.processmodel.ui.UIProperties;

public abstract class Action
{
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Fields
	//
	// /////////////////////////////////
	protected Action nextAction;

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Properties
	//
	// /////////////////////////////////
	public void setNextAction(Action nextAction)
	{
		this.nextAction = nextAction;
	}

	@UIProperties(category = "Flow")
	public Action getNextAction()
	{
		return nextAction;
	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Construction
	//
	// /////////////////////////////////
	public Action(Action afterAction)
	{
		if (afterAction != null)
			afterAction.nextAction = this;
	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Methods
	//
	// /////////////////////////////////

	// Returns next action to execute, null if user input required
	public abstract Action execute(IProcessState state, String command);

}
