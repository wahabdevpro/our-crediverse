package hxc.utils.processmodel;

import com.concurrent.hxc.Number;

public class ContactNumber extends com.concurrent.hxc.Number
{
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Fields
	//
	// /////////////////////////////////
	private String name;

	public ContactNumber(Number member, String name)
	{
		this.name = name;
		super.addressDigits = member.getAddressDigits();
		super.numberType = member.getNumberType();
		super.numberPlan = member.getNumberPlan();
	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Properties
	//
	// /////////////////////////////////
	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Construction
	//
	// /////////////////////////////////

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Methods
	//
	// /////////////////////////////////
	@Override
	public String toString()
	{
		return name == null || name.length() == 0 ? super.toString() : name;
	}

}
