package hxc.connectors.hux;

import java.io.IOException;

import hxc.connectors.IConnection;
import hxc.utils.xmlrpc.XmlRpcRequest;

public class HuxConnection implements IConnection
{
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Properties
	//
	// /////////////////////////////////
	private XmlRpcRequest rpcRequest;
	private HuxConnector connector;

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Construction
	//
	// /////////////////////////////////
	public HuxConnection(HuxConnector connector, XmlRpcRequest rpcRequest)
	{
		this.rpcRequest = rpcRequest;
		this.connector = connector;
	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// IConnection Implementation
	//
	// /////////////////////////////////
	@Override
	public void close() throws IOException
	{

	}

	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Methods
	//
	// /////////////////////////////////
	public void display(HuxProcessState huxProcessState)
	{
		connector.display(rpcRequest, huxProcessState);
	}

}
