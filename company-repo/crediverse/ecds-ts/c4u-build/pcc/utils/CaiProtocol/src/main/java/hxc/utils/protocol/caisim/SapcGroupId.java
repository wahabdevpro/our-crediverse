package hxc.utils.protocol.caisim;

/**
 * Represents a SAPC Group ID.
 * 
 * @author petar
 *
 */
public class SapcGroupId
{
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Properties
	//
	// /////////////////////////////////
	
	private String id = new String();
	
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Constructors
	//
	// /////////////////////////////////
	
	public SapcGroupId()
	{
		
	}
	
	public SapcGroupId(String id)
	{
		this.id = id;
	}
	
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Accessor Method
	//
	// /////////////////////////////////

	public String getId()
	{
		return id;
	}
	
	public void setId(String id)
	{
		this.id = id;
	}
}
