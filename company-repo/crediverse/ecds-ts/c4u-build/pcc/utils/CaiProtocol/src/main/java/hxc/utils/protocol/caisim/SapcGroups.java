package hxc.utils.protocol.caisim;

/**
 * Represents a list of SAPC Groups.
 * 
 * @author petar
 *
 */
public class SapcGroups
{
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Properties
	//
	// /////////////////////////////////
	
	private SapcGroup[] groups = new SapcGroup[0];
	
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Constructor
	//
	// /////////////////////////////////
	
	public SapcGroups()
	{
		
	}
	
	public SapcGroups(SapcGroup[] groups)
	{
		this.groups = groups;
	}
	
	// //////////////////////////////////////////////////////////////////////////////////////
	//
	// Accessor Method
	//
	// /////////////////////////////////

	public SapcGroup[] getGroup()
	{
		return groups;
	}

	public void setGroup(SapcGroup[] groups)
	{
		this.groups = groups;
	}
}